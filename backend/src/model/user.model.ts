import mongoose from "mongoose";

const UserSchema = new mongoose.Schema(
  {
    userName: {
      firstName: {
        type: String,
        required: true,
        trim: true,
      },
      lastName: {
        type: String,
        required: true,
        trim: true,
      },
    },
    email: {
      type: String,
    },
    role: {
      type: String,
      enum: ["admin", "user"],
      default: "user",
    },
  },
  { timestamps: true }
);
export default mongoose.model("Users", UserSchema);
